#A program that takes 2 numbers as inputs and performs some basic Mathematical Operations

a = input("Enter the first number: ") #Inputs the 1st number
b = input("Enter the second number: ") #Inputs the 2st number

print("Addition: ", float(a) + float(b)) #ADDITION
print("Subtraction: ", float(a) - float(b)) #SUBTRACTION
print("Multiplication: ", float(a) * float(b)) #MULTIPLICATION
print("Division: ", float(a) / float(b)) #DIVISION
